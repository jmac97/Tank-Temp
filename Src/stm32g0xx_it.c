/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */

  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
  oneMSCounter--;
  if(oneMSCounter == 0) {
    oneMSFlag = true;
    oneMSCounter = ONE_MS;
  }
  
  fiveMSCounter--;
  if(fiveMSCounter == 0) {
    fiveMSFlag = true;
    fiveMSCounter = FIVE_MS;
  }
  
  tenMSCounter--;
  if(tenMSCounter == 0) {
    tenMSFlag = true;
    tenMSCounter = TEN_MS;
  }
  
  fiftyMSCounter--;
  if(fiftyMSCounter == 0) {
    fiftyMSFlag = true;
    fiftyMSCounter = FIFTY_MS;
  }
  
  hundredMSCounter--;
  if(hundredMSCounter == 0) {
    hundredMSFlag = true;
    hundredMSCounter = HUNDRED_MS;
  }
  
  oneSCounter--;
  if (oneSCounter == 0) {
    oneSFlag = true;
    oneSCounter = ONE_S;
  }

  /* USER CODE END SysTick_IRQn 1 */
}

